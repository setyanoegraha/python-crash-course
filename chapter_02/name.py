# Assign a name to the variable
name = "lord griffin"

# Print the name with title case (first letter of each word capitalized)
print(name.title())  # Output: "Lord Griffin"

# Print the name in all lowercase letters
print(name.lower())  # Output: "lord griffin"

# Print the name in all uppercase letters
print(name.upper())  # Output: "LORD GRIFFIN"

